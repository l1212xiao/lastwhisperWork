package com.lyx.geek.AlgoMei.listnode;

/**
 * @author lvyunxiao
 * @date 2021/8/19
 * @description
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}


